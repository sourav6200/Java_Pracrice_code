import java.util.*;
public class OperaterPrecedent {
    public static void main(String[] args) {
        Scanner in =new Scanner(System.in);
        int a,b,d,k;
        System.out.println("Enter the value of a,b,d");
        a=in.nextInt();
        b=in.nextInt();
        d=in.nextInt();
k= a*b-d;
System.out.println("a*b-d="+k);
    }
}
